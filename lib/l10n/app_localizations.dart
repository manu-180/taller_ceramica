  "confirmCancelationTitle": "Potwierdź anulowanie",
  "cancelEnrollmentSuccess": "Pomyślnie anulowano zapis.",
  "cancelWaitlistSuccess": "Pomyślnie anulowano miejsce na liście oczekujących."
}

  };

  String translate(String key, {Map<String, String>? params}) {
    String translation = _localizedStrings[locale.languageCode]?[key] ?? key;

    if (params != null) {
      params.forEach((key, value) {
        translation = translation.replaceAll('\$$key', value);
      });
    }

    return translation;
  }
}

class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) => ['en', 'es', "fr", "pt", "de", "it", "zh", "ja", "ko", "ar", "hi", "ru", "tr", "nl", "sv", "pl"].contains(locale.languageCode);

  @override
  Future<AppLocalizations> load(Locale locale) {
    return SynchronousFuture<AppLocalizations>(AppLocalizations(locale));
  }

  @override
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) =>
      false;
}
